class LetterZ{
	public static void main(String[]args){
	int c=11;
		for(int i=0; i<=11 ; i++){
			for(int j=0; j<=11 ; j++){
				if(i>=0&& i<2||i>9&&i<=11||i>=2&&i<=9&&j==c)
					
					
				System.out.print("*");
			
 			else
			{
				System.out.print(" ");		
				}					
				}
				c=c-1;
			System.out.println();
		}
	}
}

	